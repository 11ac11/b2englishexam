    <h1>Reading & Use of English - Part 2</h1>
    <h2>
      What is reading and use of english part 2 like in the First Certificate
      exam?
    </h2>

    <p>
      In reading/use of english part 2 you have a gap in the text and you have
      to think of the missing word - no multiple choice this time!
    </p>

    <h2>Quick tips for reading and use of english part 2</h2>
    <ul>
      <li>
        Remember the first question (0) is an example, and the answer is on the
        page. Do not get confused and answer this question.
      </li>
      <li>Read the whole text first.</li>
      <li>
        Don't use abbreviations/contractions; <span class="highlight"
          >won't = will not (2 words)</span
        >
      </li>
      <li>
        However, remember that
        <span class="highlight">can't = cannot (1 word)</span>
      </li>
      <li>
        The most common words are:
        <ul>
          <li><span class="highlight">ARTICLES</span> (the, a, an)</li>
          <li>
            <span class="highlight">RELATIVE PRONOUNS</span> (who, which, where etc)
          </li>
          <li>
            <span class="highlight">AUX VERBS</span> (will, does, has etc)
          </li>
          <li>
            <span class="highlight">CONNECTORS</span> (however, although, because
            etc)
          </li>
          <li>
            <span class="highlight">POSSESSIVE ADJECTIVES</span> (his, her, theirs
            etc)
          </li>
          <li><span class="highlight">COMPARISONS</span> (than, as etc)</li>
          <li>
            <span class="highlight">INTENSIFIERS</span> (so, very, too etc.)
          </li>
        </ul>
      </li>
      <li>Read the whole sentence again before answering</li>
      <li>
        Be careful with verb conjugation eg. use the correct form for 3rd
        person.
      </li>
    </ul>
  </div>
</div>

<div class="example">
  <div class="text-column">
    <h1>Example Questions</h1>
    <p>
      For questions 9-16, read the text below and think of the word which best
      fits each gap. Use only one word in each gap.
    </p>
    <hr />
    <div class="reading-q">
      <h3>Motorbike stunt rider</h3>
      <p>
        I work as a motorbike stunt rider - that is, I do tricks on my motorbike
        at shows. The Le Mans race track in France was <strong
          >(9) ........</strong
        >
        I first saw some guys doing motorbike stunts. I'd never seen anyone riding
        a motorbike using just the back wheel before and I was
        <strong>(10) ........</strong> impressed I went straight home and taught
        <strong>(11) .........</strong> to do the same.
      </p>
    </div>
  </div>
  <div class="transcript">
    <div class="text-column">
      <h1>Answers</h1>
      <h2>Number (9)</h2>
      <p>
        ü§î The Le Mans race track is a place. We are looking for a <strong
          >relative pronoun</strong
        >
        here to link it to an event that happened there, so we use
        <span class="highlight2">where</span> when we talk about places..
      </p>
      <p>Answer: <span class="highlight2">where</span></p>
      <h2>Number (10)</h2>
      <p>
        After the gap we have an <strong>adjective</strong>
        (impressed), we are therefore looking for either a negative word (eg. not)
        or an <strong>intensifier</strong> (so, very, too).
      </p>
      <p>
        ‚ùå <span class="highlight2">Too</span>
        is normally used to express something so strong it becomes negative, so we
        don‚Äôt want <span class="highlight2">too</span> here.
      </p>
      <p>
        ü§î Now, <span class="highlight2">so</span> and
        <span class="highlight2">very</span>
        are quite similar. However, <span class="highlight2">so</span> is
        normally considered to describe something with greater emotion than
        <span class="highlight2">very</span>.
      </p>
      <p>
        ‚úÖ If we continue to read, he goes "straight home and teaches .........
        to do the same". This implies he was impressed with a lot of emotion to
        go immediately home and try it.
      </p>
      <p>
        Answer: <span class="highlight2">so</span>
      </p>
      <h2>Number (11)</h2>

      <p>
        ü§î The man was so impressed he went "straight home and taught ....... to
        do the same". The gap here obviously implies some form of person. It
        could be he teaches himself or maybe he teaches a friend? We need to
        read the next sentence to make sure we write the correct answer..
      </p>
      <p>
        ‚úÖ "It wasn't very long before I began to earn my living at shows
        performing my own motorbike stunts." - It is the man who learns the
        tricks. So we need to use a <strong>reflexive pronoun</strong> in the first
        person. A reflexive pronoun is when we refer back to the same thing doing
        the action.
      </p>
      <p>eg. The boy made himself a sandwich.</p>
      <p>
        The boy does the action for the boy, it is the same person so we need to
        use a reflexive pronoun.
      </p>
      <p>
        Answer: <span class="highlight2">myself</span>
      </p>
