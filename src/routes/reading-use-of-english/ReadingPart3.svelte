      </li>
      <li>
        Don't use contractions or 'not' before your word, you can only use one
        word and there will be an appropriate prefix to make it work.
      </li>
      <li>
        Look for clues in the words before the gap, for example, if before the
        gap it has <span class="highlight">a/an/the/his/her/its</span> etc. then
        you will need to transform into a noun.
      </li>
      <li>
        Be careful with verb conjugation; eg. use the correct form for 3rd
        person and plural.
