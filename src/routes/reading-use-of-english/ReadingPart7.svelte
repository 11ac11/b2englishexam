    <h1>Reading & Use of English - Part 7</h1>
    <h2>
      What is reading and use of english part 7 like in the First Certificate
      exam?
    </h2>

    <p>
      In reading/use of english part 7 you are given ten statements and a long
      text to read (or four smaller texts), divided into paragraphs. You must
      choose which statement matches which paragraph.
    </p>

    <h2>Quick tips for reading and use of english part 7</h2>
    <ul>
      <li>Read the questions first, underline key words</li>
      <li>
        Next, read the text, underline any synonyms of those key words from the
        statements, write a small number next to the word so you know which
        statement it is for.
      </li>
      <li>
        If the same word from a statement appears in a paragraph it is likely to
        be a trap and not be the correct answer
      </li>
      <li>Pay attention to the statement and which person it refers to</li>
    </ul>
  </div>
</div>

