    <h1>Reading & Use of English - Part 4</h1>
    <h2>
      What is reading and use of english part 4 like in the First Certificate
      exam?
    </h2>

    <p>
      In reading/use of english part 4 you are given a sentence and you need to
      reword the sentence using 2-5 words, including the word given EXACTLY as
      written. This is also known as word formation.
    </p>

    <h2>Quick tips for reading and use of english part 4</h2>
    <ul>
      <li>
        You must include the word <strong>EXACTLY</strong> as written. You cannot
        change a verb into past tense or into 3rd person or into a gerund for example.
      </li>
      <li>
        Don‚Äôt spend too much time on this, if you aren‚Äôt sure of the answer
        early on, just try your best and come back to it after you‚Äôve finished
        all other parts.
      </li>
      <li>
        Don't use abbreviations/contractions. Remember <span class="highlight"
          >don't = do not (2 words)</span
        >
        but <span class="highlight">can't = cannot (1 word)</span>
      </li>

      <li>Spelling is important</li>
      <li>
        Try to make the sentences as similar as possible, if one sentences uses
        'very', use 'very' in your transformed sentence (if not over the word
        limit)
      </li>
      <li>
        You can try to cancel out words that appear in both sentences, so you
        can see what you need to transform.
      </li>
    </ul>
