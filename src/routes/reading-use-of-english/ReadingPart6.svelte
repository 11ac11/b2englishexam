    <h1>Reading & Use of English - Part 6</h1>
    <h2>
      What is reading and use of english part 6 like in the First Certificate
      exam?
    </h2>

    <p>
      In reading/use of english part 6 you are given a piece of text to read
      with some missing sentences. You must then select 6 out of 7 sentences and
      put in the correct space. You do not need 1 of the additional sentences.
    </p>

    <h2>Quick tips for reading and use of english part 6</h2>
    <ul>
      <li>Read the whole text first before you even look at the questions.</li>
      <li>Start with the easiest ones first, don't worry about the order.</li>
      <li>Sometimes question 6 of part 6 is about the text as a whole</li>
      <li>
        The sentences before and after the space are the most important things.
        Look here for pronouns (it, he, she, they etc.) that match.
      </li>
    </ul>
  </div>
</div>

