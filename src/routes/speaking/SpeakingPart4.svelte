    <h1>Speaking - Part 4</h1>
    <h2>What is speaking part 4 like in the B2 First Certificate exam?</h2>

    <p>
      In part 4, you are asked to expand your conversation from part 3, using
      prompts from the examiner. You have to talk together to make a decision.
      You should talk for about 4 minutes.
    </p>
    <p>
      The examiner will ask you a question. Look at the examiner while you
      answer the question. Explain why you think what you are saying. Ask your
      partner their opinion.
    </p>

    <h3>Who do you speak to?</h3>
    <p>The examiner and your partner</p>
    <h3>How long do I speak for?</h3>
    <p>About 4 minutes.</p>
    <h3>What to practice?</h3>
    <p>Expressing and justifying opinions, agreeing and disagreeing.</p>
