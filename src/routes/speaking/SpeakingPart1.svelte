    <h1>Speaking - Part 1</h1>
    <h2>What is speaking part 1 like in the B2 First Certificate exam?</h2>

    <p>
      Speaking part one is like a personal interview. The examiner will ask you
      some questions about yourself. You do not need to speak to your partner in
      part 1.
    </p>
    <p>Example topics for questions:</p>

    <ul>
      <li>Your daily life/routine</li>
      <li>Where you live/your country</li>
      <li>Education and work</li>
      <li>Travel and holidays</li>
      <li>Sports</li>
      <li>Entertainment</li>
      <li>Family/friends</li>
      <li>Free time/hobbies</li>
      <li>The future</li>
    </ul>

    <h3>Who do you speak to?</h3>
    <p>The examiner.</p>
    <h3>How long do I speak for?</h3>
    <p>About 2 minutes.</p>
    <h3>What to practice?</h3>
    <p>Giving information about yourself and expressing your opinion.</p>
  </div>
</div>

<div class="example">
  <div class="text-column">
    <h1>Example Questions</h1>

    <h3>Where You Live Now and Your Home Country</h3>
    <p>Is there anything you would like to learn about your country?</p>
    <p>Which area of your country would you like to get to know better?</p>

    <h3>Daily Life</h3>
    <p>Tell us about a day you've really enjoyed recently.</p>
    <p>Are you planning to do anything special this weekend?</p>

    <h3>Education and Work</h3>
    <p>Are you happier doing mental or physical work?</p>
    <p>Can you remember your first English lessons?</p>

    <h3>Travel and Holidays</h3>
    <p>Have you ever used English while traveling?</p>
    <p>Is there a good public transport system in your country?</p>

    <h3>Sports</h3>
    <p>How much exercise do you do each week?</p>
    <p>Do you like to be physically active or do you prefer relaxing?</p>

    <h3>Entertainment</h3>
    <p>Do you ever go to concerts?</p>
    <p>Do you use the internet much?</p>

    <h3>Family and Friends</h3>
    <p>Who are the most important people in your life?</p>
    <p>What do you enjoy doing with your friends?</p>

    <h3>Free Time</h3>
    <p>Is your routine at weekends different from your daily routine?</p>
    <p>Does anyone you know have an interesting hobby?</p>

    <h3>The Future</h3>
    <p>Is there anything you'd love to be able to do in the future?</p>
    <p>Do you plan to study anything in the future?</p>
