    <h1>Speaking - Part 3</h1>
    <h2>What is speaking part 3 like in the B2 First Certificate exam?</h2>

    <p>
      In part 3, you are asked to have a conversation with your partner. The
      examiner will give you a task to do together and a piece of paper with a
      central statement and some ideas to discuss. You must talk to your partner
      in part 3. You should talk together for about 2 minutes and then you will
      be asked to make a decision which should last 1 minute.
    </p>

    <h3>Who do you speak to?</h3>
    <p>Your partner</p>
    <h3>How long do I speak for?</h3>
    <p>2 minutes and then 1 minute</p>
    <h3>What to practice?</h3>
    <p>
      Exchanging ideas, expressing and justifying opinions, agreeing and/or
      disagreeing, suggesting, speculating, evaluating, reaching a decision
      through negotiation, etc.
    </p>
  </div>
