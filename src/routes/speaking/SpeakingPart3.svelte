<div class="example">
  <div class="text-column">
    <h1>Useful Phrases</h1>

    <h3>Exchanging ideas and expressing/justifying opinions</h3>
    <ul>
      <li>I think this would be a good/bad option because…</li>
      <li>I reckon this is a great/terrible option because…</li>
      <li>Do you think this one would be any good?</li>
      <li>
        I think either of these two could be fantastic options as they are both…
      </li>
    </ul>

    <h3>Agreeing/disagreeing</h3>
    <ul>
      <li>No way! I definitely wasn't thinking that.</li>
      <li>Absolutely, I was going to say the same thing.</li>
      <li>How funny, I was just thinking that too!</li>
      <li>Well, maybe, but what about…</li>
      <li>Yes, totally. I'm 100% with you on that.</li>
      <li>Yeah, I agree.</li>
      <li>Sorry, but I'm not sure I agree here.</li>
    </ul>

    <h3>Suggesting</h3>
    <ul>
      <li>What do you think about this one?</li>
      <li>Do you have any thoughts on this option?</li>
      <li>I'm not sure about this one, what about you?</li>
      <li>
        I'd probably suggest this one as one of the main ones. How do you feel
        about that?
      </li>
    </ul>

    <h3>Speculating</h3>
    <ul>
      <li>This one might be good…</li>
      <li>That one could be a terrific idea!</li>
      <li>This one probably wouldn't do very well…</li>
      <li>Do you think that one could work?</li>
      <li>This one may be a better option.</li>
    </ul>
    <h3>Reaching a decision</h3>
    <ul>
      <li>Are we both in favour of this one?</li>
      <li>Let's agree to disagree.</li>
      <li>Well, I definitely think it's not this one.</li>
      <li>So then, which do you think is the best one?</li>
    </ul>
  </div>
