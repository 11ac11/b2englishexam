    <h1>Useful Phrases</h1>

    <h3>Comparisons</h3>
    <ul>
      <li>
        <span>Additionally</span> - Photo one shows <i>this</i>, additionally
        photo two shows something similar.
      </li>
      <li>
        <span>As well as</span> - Photo two as well as photo one shows
        <i>this</i>.
      </li>
      <li>
        <span>Just as</span> - Just as in photo one, photo two also shows
        <i>this</i>.
      </li>
      <li>
        <span>Similarly</span> - Photo one shows <i>this</i>. Similarly, so does
        photo two.
      </li>
      <li>
        <span>In the same way</span> - Photo one shows <i>this</i> in the same way
        as photo two.
      </li>
      <li>
        <span>In common</span> - Both photo one and photo two have things in common.
      </li>
      <li><span>They share</span> - Both photos share <i>this</i>.</li>
      <li>
        <span>They resemble</span> - Both photos resemble similar situations.
      </li>
      <li>
        <span>As with</span> - As with photo one, photo two also shows
        <i>this</i>.
      </li>
      <li>
        <span>Equally</span> - Photo 1 shows <i>this</i>, equally as does photo
        2.
      </li>
    </ul>

    <h3>Contrasts</h3>
    <ul>
      <li>
        <span>However</span> - Photo one shows <i>this</i>. However, photo 2
        shows something different.
      </li>
      <li>
        <span>Although</span> - Photo one shows <i>this</i> although photo 2 doesn't.
      </li>
      <li>
        <span>Whereas</span> - Whereas photo one shows <i>this</i>, photo two is
        completely different.
      </li>
      <li>
        <span>In contrast</span> - In contrast, both photos are showing different
        things.
      </li>
      <li>
        <span>Yet</span> - Photo one is definitely showing <i>this</i>, yet
        photo two isn't.
      </li>
      <li>
        <span>Despite <i>this</i></span> - Photo one shows <i>this</i> well.
        Despite <i>this</i>, photo two isn't sharing the same thing.
      </li>
      <li>
        <span>Though</span> - Photo one shows <i>this</i> though photo two doesn't.
      </li>
      <li>
        <span>While</span> - While <i>this</i> is happening in photo one, something
        different is happening in photo two.
      </li>
      <li>
        <span>Conversely</span> - In photo one <i>this</i> is happening, conversely
        something else is happening in photo two.
      </li>
    </ul>

    <h3>Speculating</h3>
    <ul>
      <li>It might be…</li>
      <li>It could be…</li>
      <li>There may be…</li>
      <li>She must be…</li>
    </ul>

    <h3>Expressing Opinion</h3>
    <ul>
      <li>For me…</li>
      <li>In my opinion…</li>
      <li>I think…</li>
      <li>For most people, including myself…</li>
      <li>I actually think that…</li>
    </ul>
