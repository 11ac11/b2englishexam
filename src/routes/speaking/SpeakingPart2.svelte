    <h1>Speaking - Part 2</h1>
    <h2>What is speaking part 2 like in the B2 First Certificate exam?</h2>

    <p>
      Speaking part 2 is also known as 'long-turn'. You are given two photos and
      a statement to help you compare them. You must compare the photos, not
      just describe them. Then, your partner will be asked a question on the
      same theme as the photos and they will be expected to talk for about 30
      seconds. Your partner will then get a different pair of photos, they will
      speak for a minute and when they finish, you will be asked a question
      about their photos. You should also speak for about 30 seconds.
    </p>

    <h3>Who do you speak to?</h3>
    <p>The examiner.</p>
    <h3>How long do I speak for?</h3>
    <p>1 minute and then 30 seconds</p>
    <h3>What to practice?</h3>
    <p>
      Talking on your own about something: comparing, describing, expressing
      opinions, speculating.
    </p>
