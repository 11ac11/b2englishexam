    <h1>Listening - Part 2</h1>
    <h2>What is listening part 2 like in the First Certificate exam?</h2>
    <p>
      In listening part 2 you have to complete 10 sentences with a missing word.
      As with all the parts of the listening section, you will get two
      opportunities to listen.
    </p>
    <h2>Quick tips for listening part 2</h2>
    <ul>
      <li>
        You won't hear the sentences exactly how they are written in the audio,
        so you need to listen for key words and other ways of saying the same
        thing to get the correct answer.
      </li>
      <li>
        The questions are in order, so the audio will say the answer to question
        1 before it says the answer to question 2 etc.
      </li>
    </ul>
  </div>
</div>
