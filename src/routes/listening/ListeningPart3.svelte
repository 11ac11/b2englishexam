    <h1>Listening - Part 3</h1>
    <h2>What is listening part 3 like in the First Certificate exam?</h2>
    <p>
      You will hear 5 people speak about the same topic and a list of 8 possible
      sentences. You must then match the speakers to one of the correct answers
      provided.
    </p>
    <h2>Quick tips for listening part 3</h2>
    <ul>
      <li>
        A lot of speakers will speak about three or four things on the list,
        these are called distractors. Uou need to listen carefully to see which
        is most relevant to the question.
      </li>
      <li>Look for keywords, remember the meanings of your linkers.</li>
      <li>
        Think of synonyms and ways to paraphrase the sentences given and listen
        for them.
      </li>
      <li>Again, watch out for those DISTRACTORS!</li>
    </ul>
  </div>
