    <h1>Example Question</h1>
    <p>
      You will hear people talking in eight different situations. For questions
      1-8, choose the best answer (A, B or C)
    </p>
    <hr />
    <p><span>1.</span> You hear a message on a telephone answering machine.</p>
    <p>Why is the speaker calling?</p>
    <ul>
      <li><span>A</span> to confirm some arrangements</li>
      <li><span>B</span> to issue an invitation</li>
      <li><span>C</span> to persuade someone to do something</li>
    </ul>
    <p>audio here</p>
    <h1>Answer</h1>
    <h2>Transcript</h2>
    <p class="transcript">
      Oh, hi! It's me, John. Sorry to miss you, you must have already left for
      work. Look, I wondered if you wanted to come away for the weekend?
      There'll be about ten of us including someone called Sam Brent who says he
      was at college with you, and he remembers that you were brilliant at rock
      and roll. Er, they've decided it will be on October 9th, Friday night til
      Sunday. Anyway, I'll put the details in the post. I know you're busy at
      the moment, <span>so don't feel you have to come.</span> But, just let me know
      one way or the other when you can. Talk to you soon, bye!
    </p>
    <p>
      In this recording there is no language relating to confirmation, so we
      know immediately it is not (A). <span>I wondered if you wanted toâ€¦</span>
      is a way to invite people, so it looks like it could be (B), but we need to
      listen to it all to make sure. We can confirm it's (B) when the speaker says
      <span>don't feel you have to come</span>, this is non-persuasive language
      and allows the recipient of the message to choose.
    </p>
