    <h1>Listening - Part 1</h1>
    <h2>What is listening part 1 like in the First Certificate exam?</h2>
    <p>
      In listening part 1 you get 8 questions that are unrelated. You need to
      choose the correct answer from A, B or C. As with all the parts of the
      listening section, you will get two opportunities to listen.
    </p>

    <h2>Quick tips for listening part 1</h2>
    <ul>
      <li>
        All of the answers will have keywords that are mentioned in the audio.
        You're job is to listen carefully and decide which is the correct
        answer. These are called distractors (see below for more information)
      </li>
      <li>
        Revise your knowledge of linkers/connectors. They will use a lot of
        contradicting linkers to show an opposing point of view. If you don't
        understand these, you might miss the proper meaning of the sentence.
      </li>
    </ul>
  </div>
