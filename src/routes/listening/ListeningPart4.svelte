    <h1>Listening - Part 4</h1>
    <h2>What is listening part 4 like in the First Certificate exam?</h2>
    <p>
      You will hear a monologue (one person talk for a long(ish) amount of time)
      or a conversation between two people for around 3 minutes. You then have
      to answer 8 multiple choice questions.
    </p>
    <h2>Quick tips for listening part 4</h2>
    <ul>
      <li>
        This is to test how well you can follow a conversation, there aren't as
        many distractors but there might still be some.
      </li>
      <li>
        The questions are in order of the conversation, so the answer to
        question one will happen before question two etc.
      </li>
    </ul>
  </div>
</div>
<div class="example">
  <div class="text-column">
    <h1>Example Question</h1>
    <p>
      You will hear part of a radio interview with a woman called Rachel Reed,
      who works in a cmmercial art gallery, a shop which sells works of art. For
      questions 24-30, choose the best answer (A, B or C).
    </p>
    <hr />
    <p><span>24</span> What does Rachel say about her job title?</p>
    <ol type="A">
      <li>It makes her feel more important than she is</li>
      <li>It gives people the wrong idea about her work</li>
      <li>It is appropriate for most the work she does</li>
    </ol>
    <p>audio here</p>
    <h1>Answer</h1>
    <h2>Transcript</h2>
    <span class="transcript">
      <p>
        <strong>MAN:</strong> This evening in our series 'Careers With a Difference',
        our guest is Rachel Reed who works for a small commercial art gallery. Rachel,
        Welcome.
      </p>
      <p><strong>RACHEL:</strong> Hello</p>
      <p><strong>MAN:</strong> Rachel, What exactly do you do?</p>
      <p>
        <strong>RACHEL:</strong> Well, there's two great things about working
        for a really small company. Firstly, you get to do a bit of everything.
        The other is that you can practically invent your job title - mine is
        marketing manager, although I do a lot of other things too.
        <span>It does describe the majority of what I do.</span>
      </p>
    </span>
    <p>The answer is clearly (C).</p>
