    <h1>Structure</h1>
    <h2>First block: Title</h2>
    <p>Formal, short and clear (all your subheadings should be short and clear too)</p>

    <h2>Paragraph 1: Introduction (with subheading)</h2>
    <p>The subheading for this can simply be 'Introduction'</p>
    <p>Start with: The aim/purpose of this report is to… or This report aims to outline/assess…</p>

    <h2>Paragraph 2: Positive things (with subheading above)</h2>
    <p>Useful phrases for the next 3 paragraphs: Therefore, Because of this, Due to this, In order to, so that, As of consequence of, This has resulted in.</p>

    <h2>Paragraph 3: Negative things (with subheading above)</h2>

    <h2>Paragraph 4: Recommendation/conclusion (with subheading above)</h2>
    <p>The subheading for this can simply be 'Conclusion' or 'Recommendation'</p>
    <p>Use phrases like:</p>
      <ul>
        <li>I suggest…</li>
        <li>I recommend…</li>
        <li>You might wish to consider…</li>
        <li>It would be beneficial to change…</li>
        <li>I'd advise you (not) to…</li>
