    <h1>Structure</h1>
    <p>As reviews can vary quite dramatically, I've provided three examples below; a film, restaurant and laptop/phone.    </p>
    <h2>First block: Title</h2>
    <p>Make this interesting and fun!</p>
    <ul>
      <li>Film: “A truly mesmerising performance!”</li>
      <li>Restaurant: “The newest establishment on the block means business!”</li>
      <li>Device: “Say goodbye to your old smartphone, its useless compared to this!”</li>
    </ul>

    <h2>Paragraph 1: Introduction</h2>
    <p>Introduce the thing you are reviewing, you can make this personal, e.g.</p>
    <p>I've always wanted to try/have/visit/see/read…</p>
    <p>Or you can even use a rhetorical question, e.g.</p>
    <p>Did you know… / Have you ever wanted…</p>

    <h2>Paragraph 2: General information</h2>
    <p>Give some general background information about the thing you are reviewing, e.g.</p>
    <ul>
      <li>Film: Which actors? Who directed it? Where is it set? What is the film about?</li>
      <li>Restaurant: Where is it? What type of food? How many of these restaurants are there?</li>
      <li>Device: What year is it available? Are people excited for it? why? What's different from the last version</li>
    </ul>

    <h2>Paragraph 3: Focus point 1 </h2>
    <p>Begin to review something more specific than in paragraph 2. e.g.</p>
    <ul>
      <li>Film: The storyline/plot</li>
      <li>Restaurant: The seafood specials</li>
      <li>Device: The screen resolution</li>
    </ul>


    <h2>Paragraph 4: Focus point 2</h2>
    <p>Focus on a different thing/part/feature, e.g.</p>
    <p>Use phrases like:</p>
      <ul>
        <li>Film: The actor's performance</li>
        <li>Restaurant: The outdoor terrace</li>
        <li>Device: The top quality camera</li>
      </ul>

    <h2>Paragraph 5: Recommendation</h2>
      <p>Use phrases like:</p>
      <ul>
        <li>If you loved ______, I totally recommend this!</li>
        <li>I wish I had watched it sooner, you won't regret it!</li>
        <li>Give it a try, you won't want to miss out!</li>
      </ul>
      <p>Or if you don't want to recommend it:</p>
      <ul>
        <li>Even if you liked _______, I would give this one a miss.</li>
        <li>Although __________ was amazing, the second attempt was nowhere near as good, I wouldn't waste your time.</li>
      </ul>


